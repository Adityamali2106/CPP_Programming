#include<iostream>
using std :: cout;
using std :: cin;
using std :: endl;

class PatternPrinting
{
public:
	//////////////////////////////////////////////////////////////////////////
	//
	//  Function Name : Pattern
	//  Discription :   It is used to Display Pattern like $	*
	//  Input :         Integer
	//  Output :        void
	//  Author :        Aditya Mali
	//  Date :          27/10/2025
	//
	//////////////////////////////////////////////////////////////////////////
	
	void Pattern(int iNo)
	{
		int iCnt = 0;

		if (iNo < 0)
		{
			iNo = -iNo;
		}

		for (iCnt = 0; iCnt < iNo; iCnt++)
		{
			cout<<"$\t*\t";
		}
		cout<<endl;
	}	// End of Pattern

};	// End of class PatternPrinting

//////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
//
//////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	PatternPrinting pobj;

	cout<<"Enter number : "<<endl;
	cin>>iValue;

	pobj.Pattern(iValue);

	return 0;
}

